#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb < 0)
		ft_putchar('-');
	if (nb == -2147483648)
		return (ft_putstr("2147483648"));
	if ((nb = (nb < 0 ? -nb : nb)) >= 10)
		ft_putnbr(nb / 10);
	ft_putchar((nb % 10) + '0');
}
