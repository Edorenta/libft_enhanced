int		ft_atoi(const char *str)
{
	int	res;
	int	sign;

	res = 0;
	while ((*str == 32) || (*str > 8 && *str < 14))
		str++;
	sign = ((*str == '-') ? -1 : 1);
	if ((*str == '-') || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		res = res * 10 - (*str++ - '0');
	return (sign * (-res));
}
